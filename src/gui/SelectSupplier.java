        if (evt.getClickCount() == 2) {
            if (grn != null) {
                grn.getsupplierMobileField().setText(String.valueOf(suppliersTable.getValueAt(row, 0)));
                grn.getsupplierNameLabel().setText(String.valueOf(suppliersTable.getValueAt(row, 1)) + " " + String.valueOf(suppliersTable.getValueAt(row, 2)));
                this.dispose();
            }
        }
