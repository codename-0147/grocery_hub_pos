    private void loadStock() {
        try {
            String query = "SELECT * FROM `returning_stock` INNER JOIN `stock` "
                    + "ON `returning_stock`.`stock_id` = `stock`.`id` INNER JOIN `grn_item` "
                    + "ON `stock`.`id` = `grn_item`.`stock_id` INNER JOIN `grn` "
                    + "ON `grn_item`.`grn_id` = `grn`.`id` INNER JOIN `product` "
                    + "ON `stock`.`product_id` = `product`.`id` INNER JOIN `supplier` "
                    + "ON `grn`.`supplier_mobile` = `supplier`.`mobile` INNER JOIN `returning_stock_status` "
                    + "ON `returning_stock`.`returning_stock_status_id` = `returning_stock_status`.`id` "
                    + "WHERE `supplier`.`mobile` = '"+supReturns.suppliersMap.get(supReturns.getsupplierEmailLabel().getText())+"' "
                    + "AND `returning_stock_status`.`name` = 'Pending'";
            ResultSet resultSet = MySQL.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel)returnStockTable.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("returning_stock.id"));
                vector.add(resultSet.getString("stock.barcode"));
                vector.add(resultSet.getString("product.name"));
                vector.add(resultSet.getString("returning_stock.qty"));
                vector.add(resultSet.getString("stock.mfd"));
                vector.add(resultSet.getString("stock.exp"));
                vector.add(resultSet.getString("grn_item.price"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            //e.printStackTrace();
            logger.log(Level.WARNING, "Exception", e);
        }
    }

