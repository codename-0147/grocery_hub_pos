    private void returnStockTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_returnStockTableMouseClicked
        int row = returnStockTable.getSelectedRow();
        
        if (evt.getClickCount() == 2) {
            if (supReturns != null) {
                try {
                    ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `return_invoice_item` "
                            + "WHERE `returning_stock_id` = '"+String.valueOf(returnStockTable.getValueAt(row, 0))+"'");
                    
                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "This return stock is already added", 
                                "Warning", JOptionPane.WARNING_MESSAGE);
                        this.dispose();
                    }else {
                        supReturns.getreturningStockIDLabel().setText(String.valueOf(returnStockTable.getValueAt(row, 0)));
                        supReturns.getstockBarcodeField().setText(String.valueOf(returnStockTable.getValueAt(row, 1)));
                        supReturns.getproductNameLabel2().setText(String.valueOf(returnStockTable.getValueAt(row, 2)));
                        supReturns.getquantityLabel().setText(String.valueOf(returnStockTable.getValueAt(row, 3)));
                        supReturns.getmfdLabel().setText(String.valueOf(returnStockTable.getValueAt(row, 4)));
                        supReturns.getexpLabel().setText(String.valueOf(returnStockTable.getValueAt(row, 5)));
                        supReturns.getbuyingPriceLabel().setText(String.valueOf(returnStockTable.getValueAt(row, 6)));
                        this.dispose();
                    }
                } catch (Exception e) {
                    //e.printStackTrace();
                    logger.log(Level.WARNING, "Exception", e);
                }
            }
        }
    }//GEN-LAST:event_returnStockTableMouseClicked

