    private void loadInvoices(String invoiceID) {
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `invoice` INNER JOIN `employee` "
                    + "ON `invoice`.`employee_email` = `employee`.`email` INNER JOIN `customer` "
                    + "ON `invoice`.`customer_mobile` = `customer`.`mobile` "
                    + "WHERE `invoice`.`invoice_status_id` = 1 OR `invoice`.`invoice_status_id` = 3 "
                    + "AND `invoice`.`id` LIKE '"+invoiceID+"%'");
            DefaultTableModel model = (DefaultTableModel)invoiceTable.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("employee.email"));
                vector.add(resultSet.getString("invoice.id"));
                vector.add(resultSet.getString("customer.mobile"));
                vector.add(resultSet.getString("invoice.date"));
                vector.add(resultSet.getString("invoice.paid_amount"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            //e.printStackTrace();
            logger.log(Level.WARNING, "Exception", e);
        }
    }

