    private void invoiceTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_invoiceTableMouseClicked
        int row = invoiceTable.getSelectedRow();
        
        if (evt.getClickCount() == 2) {
            try {
                if (returns != null) {
                    returns.getinvoiceEmpEmailLabel().setText(String.valueOf(invoiceTable.getValueAt(row, 0)));
                    returns.getinvoiceIDLabel().setText(String.valueOf(invoiceTable.getValueAt(row, 1)));
                    returns.getcustomerMobileLabel().setText(String.valueOf(invoiceTable.getValueAt(row, 2)));
                    returns.getinvoiceDateLabel().setText(String.valueOf(invoiceTable.getValueAt(row, 3)));
                    returns.getpaidAmountLabel().setText(String.valueOf(invoiceTable.getValueAt(row, 4)));

                    ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `invoice` INNER JOIN `invoice_item` "
                            + "ON `invoice`.`id` = `invoice_item`.`invoice_id` INNER JOIN `stock` "
                            + "ON `invoice_item`.`stock_id` = `stock`.`id` INNER JOIN `product` "
                            + "ON `stock`.`product_id` = `product`.`id` "
                            + "WHERE `invoice`.`id` = '"+String.valueOf(invoiceTable.getValueAt(row, 1))+"'");
                    
                    DefaultTableModel model = (DefaultTableModel) returns.getinvoiceItemTable().getModel();
                    model.setRowCount(0);
                    
                    while (resultSet.next()) {
                        Vector<String> vector = new Vector<>();
                        vector.add(resultSet.getString("stock.id"));
                        vector.add(resultSet.getString("product.name"));
                        vector.add(resultSet.getString("invoice_item.qty"));
                        vector.add(resultSet.getString("stock.price"));
                        vector.add(resultSet.getString("stock.mfd"));
                        vector.add(resultSet.getString("stock.exp"));
                        model.addRow(vector);
                    }

                    this.dispose();
                }
            } catch (Exception e) {
                //e.printStackTrace();
                logger.log(Level.WARNING, "Exception", e);
            }
        }
    }//GEN-LAST:event_invoiceTableMouseClicked

