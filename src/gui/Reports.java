    private void reportsTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_reportsTableMouseClicked
        int row = reportsTable.getSelectedRow();
        String reportID = String.valueOf(reportsTable.getValueAt(row, 0));
        String selectedReportType = String.valueOf(reportTypeComboBox.getSelectedItem());
        String reportsTypeFolder = "";
        String pdfFilePath = "";
        
        if (evt.getClickCount() == 2) {
            try {
                // Get the application's directory
                String appDir = new File("").getAbsolutePath();
                // Path to the "Invoice Reports" subfolder inside "ExportedReports"
                if (selectedReportType.equals("GRNs")) {
                    reportsTypeFolder = appDir + File.separator + "ExportedReports" + File.separator + "Grn Reports";
                }else if (selectedReportType.equals("Invoices")) {
                    reportsTypeFolder = appDir + File.separator + "ExportedReports" + File.separator + "Invoice Reports";
                }else if (selectedReportType.equals("Return Receipts")) {
                    reportsTypeFolder = appDir + File.separator + "ExportedReports" + File.separator + "Return Receipt Reports";
                }else if (selectedReportType.equals("Return Invoices")) {
                    reportsTypeFolder = appDir + File.separator + "ExportedReports" + File.separator + "Return Invoice Reports";
                }
                // Build the full file path of the invoice report PDF
                if (selectedReportType.equals("GRNs")) {
                    pdfFilePath = reportsTypeFolder + File.separator + "Grn_report_" + reportID + ".pdf";
                }else if (selectedReportType.equals("Invoices")) {
                    pdfFilePath = reportsTypeFolder + File.separator + "Invoice_report_" + reportID + ".pdf";
                }else if (selectedReportType.equals("Return Receipts")) {
                    pdfFilePath = reportsTypeFolder + File.separator + "Return_receipt_report_" + reportID + ".pdf";
                }else if (selectedReportType.equals("Return Invoices")) {
                    pdfFilePath = reportsTypeFolder + File.separator + "Return_invoice_report_" + reportID + ".pdf";
                }
                // Create a File object for the PDF
                File pdfFile = new File(pdfFilePath);
                // Check if the file exists
                if (pdfFile.exists()) {
                    // Check if the desktop is supported
                    if (Desktop.isDesktopSupported()) {
                        Desktop desktop = Desktop.getDesktop();
                        try {
                            // Open the PDF file with the default system viewer
                            desktop.open(pdfFile);
                            logger.log(Level.INFO, "Viewed PDF report: " + pdfFilePath);
                        } catch (IOException e) {
                            //e.printStackTrace();
                            logger.log(Level.WARNING, "Exception", e);
                            JOptionPane.showMessageDialog(null, "Error opening the PDF file: " + e.getMessage());
                        }
                    } else {
                        JOptionPane.showMessageDialog(null, "Desktop feature not supported, cannot open PDF.");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "PDF file does not exist: " + pdfFilePath);
                }
            } catch (Exception e) {
                //e.printStackTrace();
                logger.log(Level.WARNING, "Exception", e);
                JOptionPane.showMessageDialog(null, "Error viewing report: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_reportsTableMouseClicked

