    private void loadReports() {
        try {
            String selectedReportType = String.valueOf(reportTypeComboBox.getSelectedItem());
            String reportType = "";
            
            if (selectedReportType.equals("GRNs")) {
                reportType = "grn";
            }else if (selectedReportType.equals("Invoices")) {
                reportType = "invoice";
            }else if (selectedReportType.equals("Return Receipts")) {
                reportType = "customer_return_receipt";
            }else if (selectedReportType.equals("Return Invoices")) {
                reportType = "return_invoice";
            }else if (selectedReportType.equals("Return Receipts")) {
                reportType = "customer_return_receipt";
            }else if (selectedReportType.equals("Return Invoices")) {
                reportType = "return_invoice";
            }
            
            String query = "SELECT `id`, `date`, `employee`.`email` FROM `"+reportType+"` INNER JOIN `employee` "
                    + "ON `"+reportType+"`.`employee_email` = `employee`.`email`";
            Date dateFrom = null;
            Date dateTo = null;
            SimpleDateFormat format = new SimpleDateFormat("yyyy-MM-dd");
            
            if (dateFromDateChooser.getDate() != null) {
                dateFrom = dateFromDateChooser.getDate();
                query += " WHERE `"+reportType+"`.`date` > '"+format.format(dateFrom)+"' AND ";
            }
            
            if (dateToDateChooser.getDate() != null) {
                dateTo = dateToDateChooser.getDate();
                query += "`"+reportType+"`.`date` < '"+format.format(dateTo)+"'";
            }
            
            String sort = String.valueOf(sortByComboBox.getSelectedItem());
            query += " ORDER BY ";
            
            if (sort.equals("Date ASC")) {
                query += "`"+reportType+"`.`date` ASC";
            }else if (sort.equals("Date DESC")) {
                query += "`"+reportType+"`.`date` DESC";
            }else if (sort.equals("Employee ASC")) {
                query = query.replace(" ORDER BY ", " GROUP BY `id`, `date`, `employee`.`email` "
                        + "ORDER BY `employee`.`email` ASC, `date` DESC");
            }else if (sort.equals("Employee DESC")) {
                query = query.replace(" ORDER BY ", " GROUP BY `id`, `date`, `employee`.`email` "
                        + "ORDER BY `employee`.`email` DESC, `date` DESC");
            }
            
            ResultSet resultSet = MySQL.executeSearch(query);
            DefaultTableModel model = (DefaultTableModel)reportsTable.getModel();
            model.setRowCount(0);
            
            while(resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));
                vector.add(resultSet.getString("date"));
                vector.add(resultSet.getString("employee.email"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            //e.printStackTrace();
            logger.log(Level.WARNING, "Exception", e);
        }
    }
    
