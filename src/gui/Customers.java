    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        int row = customersTable.getSelectedRow();
        
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a customer to delete", "Warning", JOptionPane.WARNING_MESSAGE);
        }else {
            int showConfirmMessage = JOptionPane.showConfirmDialog(this, "Do you want to delete selected customer?", "Warning", 
                    JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            
            if (showConfirmMessage == JOptionPane.YES_OPTION) {
                String mobile = String.valueOf(customersTable.getValueAt(row, 0));

                try {
                    MySQL.executeIUD("DELETE FROM `customer` WHERE `mobile` = '"+mobile+"'");
                    loadCustomers("first_name", "ASC", searchField.getText());
                    reset();
                    logger.log(Level.INFO, "Successfully deleted customer: " + mobile);
                    JOptionPane.showMessageDialog(this, "Customer deleted successfully", "Success", 
                                JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    //e.printStackTrace();
                    logger.log(Level.WARNING, "Exception", e);
                }
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

