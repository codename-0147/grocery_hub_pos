    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String firstName = firstNameField.getText();
        String lastName = lastNameField.getText();
        String mobile = mobileField.getText();
        String email = emailField.getText();
        
        if (mobile.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!mobile.matches("^(?:0|94|\\+94|0094)?(?:(11|21|23|24|25|26|27|31|32|33|34|35|36|37|38|41|45|47|51|52|"
                + "54|55|57|63|65|66|67|81|91)(0|2|3|4|5|7|9)|7(0|1|2|4|5|6|7|8)\\d)\\d{6}$")) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (firstName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a first name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!firstName.matches("^[A-Za-z]+$")) {
            JOptionPane.showMessageDialog(this, "Invalid first name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (lastName.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a last name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!lastName.matches("^[A-Za-z]+$")) {
            JOptionPane.showMessageDialog(this, "Invalid last name", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter an email address", "Warning", JOptionPane.WARNING_MESSAGE);
        }else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@"
                + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
            JOptionPane.showMessageDialog(this, "Invalid email address", "Warning", JOptionPane.WARNING_MESSAGE);
        }else {
            try {
                ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` WHERE `mobile` = '"+mobile+"' "
                        + "OR `email` = '"+email+"'");
                
                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "A customer is already registered with the same mobile and or "
                            + "email", "Warning", JOptionPane.WARNING_MESSAGE);
                }else {
                    MySQL.executeIUD("INSERT INTO `customer`(`mobile`, `first_name`, `last_name`, `email`, `points`, "
                            + "`store_credit`) VALUES('"+mobile+"', '"+firstName+"', '"+lastName+"', '"+email+"', 0, 0)");
                    loadCustomers("first_name", "ASC", searchField.getText());
                    reset();
                    logger.log(Level.INFO, "Successfully added customer: " + mobile);
                    JOptionPane.showMessageDialog(this, "Customer added successfully", "Success", 
                            JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                //e.printStackTrace();
                logger.log(Level.WARNING, "Exception", e);
            }
        }
    }//GEN-LAST:event_addButtonActionPerformed

