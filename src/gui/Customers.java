    private void loadCustomers(String column, String orderBy, String mobile) {
        try {
            ResultSet resultSet = MySQL.executeSearch("SELECT * FROM `customer` "
                    + "WHERE `mobile` LIKE '"+mobile+"%' ORDER BY `"+column+"` "+orderBy+"");
            DefaultTableModel model = (DefaultTableModel)customersTable.getModel();
            model.setRowCount(0);
            
            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("first_name"));
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));
                vector.add(resultSet.getString("points"));
                model.addRow(vector);
            }
        } catch (Exception e) {
            //e.printStackTrace();
            logger.log(Level.WARNING, "Exception", e);
        }
    }
    
    private void search() {
        if (sortByComboBox.getSelectedIndex() == 0) {
            loadCustomers("first_name", "ASC", searchField.getText());
        }else if (sortByComboBox.getSelectedIndex() == 1) {
            loadCustomers("first_name", "DESC", searchField.getText());
        }else if (sortByComboBox.getSelectedIndex() == 2) {
            loadCustomers("points", "ASC", searchField.getText());
        }else if (sortByComboBox.getSelectedIndex() == 3) {
            loadCustomers("points", "DESC", searchField.getText());
        }
    }
    
